/// <summary>
/// Enumeration representing the various scenes in the game.
/// They are in the same order as the "Build Settings".
/// </summary>
public enum Scenes
{
    /// <summary>
    /// The main menu scene.
    /// </summary>
    Menu,

    /// <summary>
    /// The main game scene.
    /// </summary>
    Game,

    /// <summary>
    /// The splash screen scene for Miso.
    /// </summary>
    SplashScreenMiso,

    /// <summary>
    /// The splash screen scene for the game.
    /// </summary>
    SplashScreenGame,

    /// <summary>
    /// The loading screen scene.
    /// </summary>
    Loading
}